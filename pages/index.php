<h1>Welcome</h1>
<p>
    UFT-8 or Unicode Transformation Format 8, is a character encoding standard that uses 8-bits blocks to represent a character on the internet. It is by far the most widely used character encoder of text for website pages, because it is capable of encoding all possible characters (code points).
</p>
<p>
    One of the really nice features of UTF-8 is that it is compatible with nul-terminated strings.
</p>